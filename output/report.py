def _section_deterioration(det_summary: dict, det_narrative: Optional[str] = None) -> str:
    lines = ["## 5b. Deterioration Forecast", ""]

    lines.append("| Parameter | Value |")
    lines.append("|-----------|-------|")
    lines.append(f"| Current IRI | {det_summary.get('iri_current', 'N/A')} m/km |")
    lines.append(f"| Do-Nothing IRI (end) | {det_summary.get('iri_do_nothing_end', 'N/A')} m/km |")
    lines.append(f"| With-Project IRI (end) | {det_summary.get('iri_with_project_end', 'N/A')} m/km |")
    lines.append(f"| IRI Saving | {det_summary.get('iri_saving_end', 'N/A')} m/km |")
    lines.append(f"| Average IRI Benefit | {det_summary.get('avg_iri_saving', 'N/A')} m/km |")
    years_poor = det_summary.get("years_until_poor")
    if years_poor is not None:
        lines.append(f"| Years Until Poor (IRI 10) | {years_poor} |")
    lines.append(f"| Deterioration Rate (k) | {det_summary.get('deterioration_rate_k', 'N/A')} |")
    lines.append(f"| Post-Construction IRI | {det_summary.get('post_construction_iri', 'N/A')} m/km |")

    if det_narrative:
        lines.append("")
        lines.append(det_narrative)

    return "\n".join(lines)


def _section_sensitivity_analysis(sensitivity_results) -> str:
    lines = ["## 6. Sensitivity Analysis", ""]
    if not sensitivity_results:
        lines.append("*Sensitivity analysis not available.*")
        return "\n".join(lines)

    summary = sensitivity_results.get("summary", {})
    lines.append(f"**Most sensitive variable:** {summary.get('most_sensitive_variable', 'N/A')}")

    sv = sensitivity_results.get("switching_values", {})
    if sv:
        lines.append("")
        lines.append("### Switching Values")
        lines.append("*(Change at which NPV becomes zero)*")
        lines.append("")
        lines.append("| Variable | Switching Value |")
        lines.append("|----------|----------------|")
        for var, val in sv.items():
            if var == "traffic_growth":
                lines.append(f"| {var.replace('_', ' ').title()} | {val:+.1%} absolute |")
            else:
                lines.append(f"| {var.replace('_', ' ').title()} | {val:+.0%} |")

    scenarios = sensitivity_results.get("scenarios", {})
    if scenarios:
        lines.append("")
        lines.append("### Scenario Analysis")
        lines.append("")
        lines.append("| Scenario | NPV | BCR | Viable |")
        lines.append("|----------|-----|-----|--------|")
        base = sensitivity_results.get("base_case", {})
        lines.append(f"| Base Case | ${base.get('npv', 0):,.0f} | {base.get('bcr', 0):.2f} | Yes |")
        for name, s in scenarios.items():
            if "error" not in s:
                viable = "Yes" if s.get("viable") else "No"
                lines.append(f"| {name.replace('_', ' ').title()} | ${s.get('npv', 0):,.0f} | {s.get('bcr', 0):.2f} | {viable} |")

    lines.append("")
    lines.append(f"**Risk Assessment:** {summary.get('risk_assessment', 'N/A')}")

    return "\n".join(lines)


